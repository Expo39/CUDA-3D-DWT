#include "filters.h"

// Function to get the wavelet filters based on the filter type
bool get_filters(const std::string& filter_type, const float*& lpf, const float*& hpf, const float*& Ilpf, const float*& Ihpf, size_t& filter_size) {
    if (filter_type == "db1" || filter_type == "haar") {
        lpf = DB1_L;
        hpf = DB1_H;
        Ilpf = DB1_L;
        Ihpf = DB1_H;
        filter_size = 2;
    } else if (filter_type == "db2") {
        lpf = DB2_L;
        hpf = DB2_H;
        Ilpf = DB2_I_L;
        Ihpf = DB2_I_H;
        filter_size = 4;
    } else if (filter_type == "db3") {
        lpf = DB3_L;
        hpf = DB3_H;
        Ilpf = DB3_I_L;
        Ihpf = DB3_I_H;
        filter_size = 6;
    } else if (filter_type == "db4") {
        lpf = DB4_L;
        hpf = DB4_H;
        Ilpf = DB4_I_L;
        Ihpf = DB4_I_H;
        filter_size = 8;
    } else if (filter_type == "db5") {
        lpf = DB5_L;
        hpf = DB5_H;
        filter_size = 10;
    } else if (filter_type == "db6") {
        lpf = DB6_L;
        hpf = DB6_H;
        filter_size = 12;
    } else if (filter_type == "db7") {
        lpf = DB7_L;
        hpf = DB7_H;
        filter_size = 14;
    } else if (filter_type == "db8") {
        lpf = DB8_L;
        hpf = DB8_H;
        Ilpf = DB8_I_L;
        Ihpf = DB8_I_H;
        filter_size = 16;
    } else {
        std::cerr << "Unsupported filter type!" << std::endl;
        return false;
    }
    return true;
}

